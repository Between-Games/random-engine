const randomDigits = [];

while(randomDigits.length < 100000) {
    randomDigits.push(RandomEngine.getRandomDigit());
}

// ╔═══════╗╔═══════╗╔═══════╗╔═══════╗╔═══════╗
// ╚╗ ╔══╗ ║╚══╗ ╔══╝║ ╔═════╝╚══╗ ╔══╝╚══╗ ╔══╝
